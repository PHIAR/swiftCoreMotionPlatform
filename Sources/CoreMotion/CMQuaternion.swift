import Foundation

public struct CMQuaternion {
    public var x: Double
    public var y: Double
    public var z: Double
    public var w: Double

    public init(x: Double,
                y: Double,
                z: Double,
                w: Double) {
        self.x = x
        self.y = y
        self.z = z
        self.w = w
    }
}
